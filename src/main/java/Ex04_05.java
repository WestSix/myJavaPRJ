public class Ex04_05 {

    public static void main(String[] args) {

        int a = 100, b = 200;

        /*관계 연산자*/
        System.out.printf("%d == %d 는 %s 이다.\n", a, b, a == b);
        System.out.printf("%d != %d 는 %s 이다.\n", a, b, a != b);
        System.out.printf("%d > %d 는 %s 이다.\n", a, b, a > b);
        System.out.printf("%d < %d 는 %s 이다.\n", a, b, a < b);
        System.out.printf("%d >= %d 는 %s 이다.\n", a, b, a >= b);
        System.out.printf("%d <= %d 는 %s 이다.\n", a, b, a <= b);
        /*관계 연산자*/

        /*대입 연산자*/
        System.out.printf("%d = %d 는 %s 이다.\n", a, b, a = b);
        /*대입 연산자*/

    }

}
